package com.example.design_alley;

public class moreModel {
    Integer icons;
    String details;
    public moreModel(Integer Icons,String Details){
        this.icons =Icons;
        this.details =Details;
    }

    public Integer getIcons() {
        return icons;
    }

    public void setIcons(Integer icons) {
        this.icons = icons;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }
}
